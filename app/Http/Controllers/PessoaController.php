    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'nome' => 'required|string|max:255',
        ]);

        Pessoa::create([
            'nome' => $request->nome,
            'eh_cliente' => $request->has('eh_cliente'),
            'eh_vendedor' => $request->has('eh_vendedor'),
        ]);

        return redirect()->route('orcamentos.index')->with('success', 'Pessoa cadastrada com sucesso!');
    }

